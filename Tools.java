package pkg1;
class Tools{
    public void numbers(int n1){
        for(int i=0;i<n1;i++){
            System.out.print(i+"");
        }
        System.out.println();
    }
    public void even(int n1){
        for(int i=0;i<n1;i++){
            System.out.print(2*i+" ");
        }
        System.out.println();
    }
    public void odd(int n1){
        for(int i=0;i<n1;i++){
            System.out.print(((2*i)+1)+" ");
        }
        System.out.println();
    }
    public void multi3(int n1){
        for(int i=0;i<n1;i++){
            System.out.print(3*i+" ");
        }
        System.out.println();
    }
    public void multi4(int n1){
        for(int i=0;i<n1;i++){
            System.out.print(4*i+" ");
        }
        System.out.println();
    }
    
    public void multi5(int n1){
        for(int i=0;i<n1;i++){
            System.out.print(5*i+" ");
        }
        System.out.println();
    }
}
    
    
    